// Function to calculate the average voltage (DC)
float calculate_dc_voltage(uint32_t pulse, uint32_t period) {
    return V_IN * ((float)pulse / (float)period);
}

// Function to calculate the signal frequency
float calculate_frequency(uint32_t prescaler, uint32_t period) {
    return (float)TIMER_CLOCK_FREQ / ((prescaler + 1) * (period + 1));
}
